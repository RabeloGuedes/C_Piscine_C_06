#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int argc, char **argv)
{
	if (argc > 1)
	{
		while (argc > 1)
		{
			ft_print(argv[argc - 1]);
			argc--;
			ft_putchar('\n');
		}
	}
	return (0);
}
